let currentLanguage = 'zh-CN';
const TEXT_NODE_SOURCES = new WeakMap();
const ATTRIBUTE_SOURCES = new WeakMap();

export function normalizeUiLanguage(value, fallback = 'zh-CN') {
    const raw = String(value || '').trim();
    if (!raw) {
        return UI_LANGUAGE_OPTIONS.includes(fallback) ? fallback : 'zh-CN';
    }
    if (UI_LANGUAGE_OPTIONS.includes(raw)) {
        return raw;
    }
    const normalized = raw.toLowerCase().replace(/_/g, '-');
    return LANGUAGE_ALIASES[normalized] || (UI_LANGUAGE_OPTIONS.includes(fallback) ? fallback : 'zh-CN');
}

export function setUiLanguage(language) {
    currentLanguage = normalizeUiLanguage(language, currentLanguage);
    return currentLanguage;
}

export function getUiLanguage() {
    return currentLanguage;
}

export function getUiLanguageOptions() {
    return UI_LANGUAGE_OPTIONS.map((value) => ({
        value,
        label: UI_LANGUAGE_NATIVE_LABELS[value] || value
    }));
}

export function t(source, replacements = {}) {
    const key = String(source ?? '');
    const translated = currentLanguage === 'zh-CN'
        ? key
        : (PHRASES[currentLanguage]?.[key] || key);
    return translated.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, name) => (
        Object.prototype.hasOwnProperty.call(replacements, name)
            ? String(replacements[name])
            : match
    ));
}

function translateAttribute(element, attributeName) {
    const value = element.getAttribute(attributeName);
    if (!value) {
        return;
    }
    let sources = ATTRIBUTE_SOURCES.get(element);
    if (!sources) {
        sources = {};
        ATTRIBUTE_SOURCES.set(element, sources);
    }
    const source = sources[attributeName] || value;
    sources[attributeName] = source;
    const translated = t(source);
    if (translated !== value) {
        element.setAttribute(attributeName, translated);
    }
}

function shouldSkipNode(node, skipSelectors = []) {
    const element = node.nodeType === Node.ELEMENT_NODE ? node : node.parentElement;
    if (!element) {
        return true;
    }
    if (['SCRIPT', 'STYLE', 'SVG', 'PATH'].includes(element.tagName)) {
        return true;
    }
    if (element.closest?.('[data-i18n-skip]')) {
        return true;
    }
    return skipSelectors.some((selector) => element.closest?.(selector));
}

function translateTextNode(node) {
    const value = node.nodeValue || '';
    const trimmed = value.trim();
    if (!trimmed) {
        return;
    }
    const source = TEXT_NODE_SOURCES.get(node) || trimmed;
    TEXT_NODE_SOURCES.set(node, source);
    const translated = t(source);
    if (translated === trimmed) {
        return;
    }
    const prefix = value.match(/^\s*/)?.[0] || '';
    const suffix = value.match(/\s*$/)?.[0] || '';
    node.nodeValue = `${prefix}${translated}${suffix}`;
}

export function applyI18n(root = document, { skipSelectors = [] } = {}) {
    const doc = root.ownerDocument || root;
    if (doc?.documentElement) {
        doc.documentElement.lang = HTML_LANG[currentLanguage] || 'zh-CN';
    }

    const scanRoot = root.nodeType === Node.DOCUMENT_NODE ? root.body : root;
    if (!scanRoot) {
        return;
    }

    scanRoot.querySelectorAll?.('[data-i18n]').forEach((element) => {
        const key = element.getAttribute('data-i18n');
        if (key) {
            element.textContent = t(key);
        }
    });

    scanRoot.querySelectorAll?.('*').forEach((element) => {
        if (shouldSkipNode(element, skipSelectors)) {
            return;
        }
        ['placeholder', 'title', 'aria-label', 'alt'].forEach((attributeName) => {
            translateAttribute(element, attributeName);
        });
    });

    const walker = doc.createTreeWalker(scanRoot, NodeFilter.SHOW_TEXT, {
        acceptNode: (node) => shouldSkipNode(node, skipSelectors)
            ? NodeFilter.FILTER_REJECT
            : NodeFilter.FILTER_ACCEPT
    });

    const nodes = [];
    while (walker.nextNode()) {
        nodes.push(walker.currentNode);
    }
    nodes.forEach(translateTextNode);
}
